package ClassPractice;

public class ArrayExpection {
    public static void main(String[] args) {
        int arr[] = new int[4];
        arr[0] = 1;
        arr[1] = 13;
        arr[2] = 15;
        arr[3] = 16;
        arr[4] = 20;

        for (int i = 0; i <= arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
